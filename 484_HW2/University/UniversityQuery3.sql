
SELECT DISTINCT E1.SID
FROM Enrollments E1, Enrollments E2, Enrollments E3, 
Courses C1, Courses C2, Courses C3
WHERE (E1.CID = C1.CID AND E2.CID = C2.CID AND E3.CID = C3.CID
AND C1.C_Name = 'EECS442' AND C2.C_Name = 'EECS445' AND C3.C_Name = 'EECS492'
AND E1.SID = E2.SID AND E2.SID = E3.SID) 
OR 
(E1.CID = C1.CID AND E2.CID = C2.CID
AND C1.C_Name = 'EECS482' AND C2.C_Name = 'EECS486'
AND E1.SID = E2.SID)
OR
(E1.CID = C1.CID 
AND C1.C_Name = 'EECS482')
ORDER BY E1.SID ASC;